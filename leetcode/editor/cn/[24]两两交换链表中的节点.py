# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next
class Solution:
    def swapPairs(self, head: ListNode) -> ListNode:
        dummy_head = ListNode(next=head)
        pre = dummy_head

        while pre.next and pre.next.next:
            cur = pre.next
            post = pre.next.next

            # preï¼Œcurï¼Œpostå¯¹åº”æœ€å·¦ï¼Œä¸­é—´çš„ï¼Œæœ€å³è¾¹çš„èŠ‚ç‚¹
            cur.next = post.next
            post.next = cur
            pre.next = post

            pre = pre.next.next

        return dummy_head.next

