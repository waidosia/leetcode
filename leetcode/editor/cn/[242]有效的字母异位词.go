func isAnagram(s string, t string) bool {
	if len(s) != len(t) {
		return false
	}
	exists := make(map[byte]int)
	for i := 0; i < len(s); i++ {
		if v, ok := exists[s[i]]; ok {
			exists[s[i]] = v + 1
		} else {
			exists[s[i]] = 1
		}
	}
	for i := 0; i < len(t); i++ {
		if v, ok := exists[t[i]]; v > 0 && ok {
			exists[t[i]] = v - 1
		} else {
			return false
		}
	}
	return true
}

